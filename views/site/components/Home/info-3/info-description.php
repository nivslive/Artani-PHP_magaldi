<h1 class="text-left section-text-desc">
Três suítes em projeto de arquitetura autoral, com a assinatura inconfundível
de Arthur Casas. Madeira, pedra, muita luz, espaço e jardins contemplativos.
Um jeito completamente único de morar. O prédio foi implantado no terreno
de modo a criar o maior recuo possível em relação aos vizinhos e tirar o
melhor proveito das vistas para a cidade. Para a homogeneidade do
pavimento tipo, foram usados o caixilho e o brise em tela solar, que ajudam
na proteção acústica e térmica do edifício, conferindo uniformidade e leveza
para a fachada e dando identidade ao projeto. O terreno retangular permitiu
desfragmentar o pavimento tipo e criar um jogo de elementos em balanço,
embasando o verde em terraços ao longo do edifício. Os apartamentos têm
145m², mas a percepção de espaço é muito maior. 
</h1>


