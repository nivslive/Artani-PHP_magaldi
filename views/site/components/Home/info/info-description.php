A proposta contempla um exoesqueleto que nasce no nível da calçada,
percorrendo o edifício todo e culminando no coroado, onde a exoestrutura ganha
uma importância de cúpula de uma grande luminária na cidade e propicia uma
exuberante vista do Parque do Ibirapuera. Nossa fachada da rua nos remete aos
grandes centros e cidades do mundo, propiciando que o edifício dialogue com o
passeio público de forma singular, urbana e integrada à paisagem com respeito e
identidade.



